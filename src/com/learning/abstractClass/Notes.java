    /*

Data abstraction is the process of hiding certain details and showing only essential information to the user.
Abstraction can be achieved with either abstract classes or interfaces

The abstract keyword is a non-access modifier, used for classes and methods:
Abstract class: is a restricted class that cannot be used to create objects (to access it, it must be inherited from
another class).

Abstract method: can only be used in an abstract class, and it does not have a body. The body is provided by the subclass
(inherited from). An abstract class can have both abstract and regular methods.

-An instance of an abstract class can not be created.
-Constructors are allowed.
-We can have an abstract class without any abstract method.
-Abstract classes can also have final methods (methods that cannot be overridden)
-We are not allowed to create object for any abstract class.i.e., for abstract class instantiation is not possible.
-Similar to the interface we can define static methods in an abstract class that can be called independently without
an object.

In Java, we can have an abstract class without any abstract method. This allows us to create classes that cannot be
 instantiated but can only be inherited.
    */
